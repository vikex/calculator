package calculator;

public class Calculator {
    
    public int add(int a, int b){
        return a + b;
    }
    public int minus(int a, int b){
        return a - b;
    }
    public int mnozenie(int a, int b){
        return a * b;
    }
    public int dzielenie(int a, int b){
        return a / b;
    }
}
